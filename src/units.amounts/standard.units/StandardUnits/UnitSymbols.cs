
namespace cc.isr.UnitsAmounts.StandardUnits;

/// <summary>   A unit symbols. </summary>
/// <remarks>
/// 2023-04-08. <para>
/// <see href="http://UnicodeLookup.com/#Greek"/>
/// <see href="http://UnicodeLookup.com/#math"/>
/// </para>
/// </remarks>
public static class UnitSymbols
{
    /// <summary>   Gets the mu. </summary>
    /// <value> The mu. </value>
    public static string MU => $"{Convert.ToChar( 0x3BC )}";

    /// <summary>   Gets the eta. </summary>
    /// <value> The eta. </value>
    public static string Eta => $"{Convert.ToChar( 0x3BC )}";

    /// <summary>   Gets the omega. </summary>
    /// <value> The omega. </value>
    public static string Omega => $"{Convert.ToChar( 0x3A9 )}";

    /// <summary>   Gets the sigma. </summary>
    /// <value> The sigma. </value>
    public static string Sigma => $"{Convert.ToChar( 0x3C3 )}";

    /// <summary>   Gets the omega inverted. </summary>
    /// <value> The omega inverted. </value>
    public static string OmegaInverted => $"{Convert.ToChar( 0x2127 )}";

    /// <summary>   Gets the squared. </summary>
    /// <value> The squared. </value>
    public static string Squared => $"{Convert.ToChar( 0x2072 )}";

    /// <summary>   Gets the cubed. </summary>
    /// <value> The cubed. </value>
    public static string Cubed => $"{Convert.ToChar( 0x2073 )}";

    /// <summary>   Gets the degrees. </summary>
    /// <value> The degrees. </value>
    public static string Degrees => $"{Convert.ToChar( 0x2070 )}";

    /// <summary>   Gets the dot product. </summary>
    /// <value> The dot product. </value>
    public static string DotProduct => $"{Convert.ToChar( 0x2219 )}";

    /// <summary>   Gets the white square. </summary>
    /// <value> The white square. </value>
    public static string WhiteSquare => $"{Convert.ToChar( 0x25A1 )}";

    /// <summary>   Gets the inverted one. </summary>
    /// <value> The inverted one. </value>
    public static string InvertedOne => $"{Convert.ToChar( 0x196 )}";
}

